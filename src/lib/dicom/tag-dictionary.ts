export function lookupTag(tag: string): TagInfo {
  const upper = tag.toUpperCase()
  return DICT[upper] ?? { keyword: 'Unknown', vr: 'UN', name: 'Unknown Tag' }
}

export function toParserTag(tag: string): string {
  return 'x' + tag.toLowerCase()
}
